# Task No. 3 from Simple Decisions, this time with IF, ELIF & ELSE

print("Towards which direction should I paint (up, down, left or right)?")
dir = input()

if (dir == "up"):
    print("I'm painting in the upward direction")
elif (dir == "down"):
    print("I'm painting in the down direction")
elif (dir == "left"):
    print("I'm painting in the left direction")
elif (dir == "right"):
    print("I'm painting in the right direction")
else:
    print("I don't know on what direction I'm painting")
